public class Car {
    /**
     * 车牌号
     */
    private String number;

    /**
     * 车主
     */
    private String owner;

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getOwner() {
        return owner;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }
}
